#here we will see how to find char value in ASCII  by using ord(any char) function.

#we can find only one char in one time.

#how to find the value in uppercase 

a=ord("A")
print(a)

b=ord("B")
print(b)

#how to find the value in lowercase 


a=ord("b")
print(a)

b=ord("a")
print(b)


